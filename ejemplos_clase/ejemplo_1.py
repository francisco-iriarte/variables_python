suma = numero_1 + numero_2   # Operamos la suma
# Cargamos en la variable suma el resultado de la adición
# Imprimimos en consola el resultado
print('El resultado de la suma de ', numero_1, ' y' ,numero_2, 'es', suma)

diferencia = numero_1 - numero_2  # Operamos la resta
# Cargamos en la variable diferencia el resultado de la sustracción
# Imprimimos en consola el resultado c
print('El diferencia de restar', numero_1, 'y', numero_2, 'es', diferencia)

